        /// <summary>
        /// Gets the description for the specified DTC.
        /// </summary>
        /// <param name="dtc">The DTC.</param>
        /// <returns>A description text for the specified DTC.</returns>
        public static string GetDtcDescription(string dtc)
        {
            if (dtcDictionary == null)
            {
                InitDtcDescriptions();
            } // if

            if ((dtcDictionary != null) && dtcDictionary.ContainsKey(dtc))
            {
                return dtcDictionary[dtc];
            } // if

            // fallback
            if (dtc.StartsWith("P00"))
            {
                return "Fuel and air metering and auxiliary emission controls";
            } // if

            if (dtc.StartsWith("P01"))
            {
                return "Fuel and air metering.";
            } // if

            if (dtc.StartsWith("P02"))
            {
                return "Fuel and air metering (injector circuit)";
            } // if

            if (dtc.StartsWith("P03"))
            {
                return "Ignition system or misfire";
            } // if

            if (dtc.StartsWith("P04"))
            {
                return "Auxiliary emissions controls";
            } // if

            if (dtc.StartsWith("P05"))
            {
                return "Vehicle speed controls and idle control system";
            } // if

            if (dtc.StartsWith("P06"))
            {
                return "Computer output circuit";
            } // if

            if (dtc.StartsWith("P07"))
            {
                return "Transmission";
            } // if

            if (dtc.StartsWith("P08"))
            {
                return "Transmission";
            } // if

            return "N/A";
        } // GetDtcDescription()
